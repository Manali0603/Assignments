package com.maersk;

public class Calculator {
    public int add(int a,int b)
    {
        return a+b;
    }
    public double divide(double a,double b)
    {
        return a/b;
    }
    public double findCircleArea(double r)
    {
        return Math.PI*r*r;
    }
    public int multiply(int a,int b)
    {
        return a*b;
    }
}
